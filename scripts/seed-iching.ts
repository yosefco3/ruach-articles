async function main() {
  console.log("[seed-iching] intro…");
  await updateIchingIntro(INTRO);

  console.log("[seed-iching] 8 trigrams…");
  for (const [key, prose] of Object.entries(TRIGRAM_TEXTS)) {
    await upsertTrigramText({ trigramKey: key, description: `<p>${prose}</p>` });
  }

  console.log(`[seed-iching] ${HEXAGRAMS.length} hexagrams…`);
  for (const h of HEXAGRAMS) {
    await upsertHexagramText({
      number: h.number,
      trigramExplanation: h.trigramExplanation,
      interpretation: h.interpretation,
      changingLinesNote: h.changingLinesNote ?? "",
    });
  }

  console.log("[seed-iching] done.");
}

main()
  .then(() => process.exit(0))
  .catch((err) => {
    console.error("[seed-iching] failed:", err);
    process.exit(1);
  });
