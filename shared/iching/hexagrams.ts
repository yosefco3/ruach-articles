export interface HexagramStruct {
  number: number; // 1..64
  name: string; // מנוקד מ-HEX_NAMES
  lower: number; // ערך טריגרמה תחתונה 0..7
  upper: number; // ערך טריגרמה עליונה 0..7
  glyph: string; // גליף יוניקוד
}

/** גליף ההקסגרמה — כמו glyphFor באב-טיפוס. */
export function glyphFor(num: number): string {
  return String.fromCodePoint(0x4dbf + num);
}

// reverse: number → {lower, upper}
const REVERSE: Record<number, { lower: number; upper: number }> = {};
for (const k of Object.keys(HEX_LOOKUP)) {
  const [lower, upper] = k.split("-").map(Number);
  REVERSE[HEX_LOOKUP[k]] = { lower, upper };
}

export function hexagramByNumber(num: number): HexagramStruct | undefined {
  const rv = REVERSE[num];
  if (!rv) return undefined;
  return { number: num, name: HEX_NAMES[num], lower: rv.lower, upper: rv.upper, glyph: glyphFor(num) };
}

/** ערכי טריגרמה תחתונה+עליונה → הקסגרמה (ליבת הזיהוי בהטלה). */
export function hexagramByTrigrams(lower: number, upper: number): HexagramStruct | undefined {
  const num = HEX_LOOKUP[lower + "-" + upper];
  return num ? hexagramByNumber(num) : undefined;
}

export const HEXAGRAMS: HexagramStruct[] = Array.from(
  { length: 64 },
  (_, i) => hexagramByNumber(i + 1)!,
);
